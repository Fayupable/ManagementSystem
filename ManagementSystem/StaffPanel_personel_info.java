    private void tbl_staffPanel_personel_infoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_staffPanel_personel_infoMouseClicked

        if (tbl_staffPanel_personel_info.getSelectedRow() < 0) {
            return;
        }
        int id = (int) tbl_table_staffE_model.getValueAt(tbl_staffPanel_personel_info.getSelectedRow(), 0);

        Staff stff = CompanyDB.SearchStaffById(id);
        if (stff != null) {
            txt_staffPanel_personel_info_id.setText(stff.id + "");
            txt_staffPanel_personel_info_name_surname.setText(stff.name_surname);
            txt_staffPanel_personel_info_email.setText(stff.email);
            txt_staffPanel_personel_info_address.setText(stff.address);
            txt_staffPanel_personel_info_password.setText(stff.password);
            cmbx_staffPanel_gender.setSelectedItem(stff.gender);
            cmbx_staffPanel_personel_info_position.setSelectedItem(stff.position);
            txt_staffPanel_personel_info_salary.setText(stff.salary + "");

        }


    }//GEN-LAST:event_tbl_staffPanel_personel_infoMouseClicked

