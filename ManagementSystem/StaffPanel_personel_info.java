    private void btn_staffPanel_personel_info_verifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_staffPanel_personel_info_verifyActionPerformed

        String email = txt_staffPanel_personel_info_email.getText();
        String password = txt_staffPanel_personel_info_password.getText();
        Staff staff = CompanyDB.StaffLogin(email, password);

        if (email.isEmpty() && password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your information in the boxes ");

        } else {
            if (staff != null) {
                JOptionPane.showMessageDialog(rootPane, "Verification successful");

                tbl_table_staffE_model.setRowCount(0);
                ArrayList<Staff> slist = CompanyDB.SearchByNameOrEmailStaffEdit(email);
                for (Staff stff : slist) {

                    tbl_table_staffE_model.addRow(new Object[]{stff.id, stff.name_surname, stff.email, stff.password, stff.address, stff.gender, stff.position, stff.salary});

                }

            } else {
                JOptionPane.showMessageDialog(rootPane, "Verification failed");
            }

        }


    }//GEN-LAST:event_btn_staffPanel_personel_info_verifyActionPerformed

